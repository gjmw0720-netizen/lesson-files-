# 入力されたパスワードを受け取る
input_password = input("パスワードを入力してください: ")

# パスワードの判定
# 【1つ目の穴埋め】入力された変数と、正解の文字列を「==」で比較します
if input_password == "admin123":
    print("ログイン成功")

    # 商品一覧
    items = ["ノートPC", "マウス", "モニター"]
    print("\n【在庫商品一覧】")

    # 商品を順番に表示
